package com.example.fashionecommerce.model;

public class ItemSuggest {
    String name;
    String type;

    public ItemSuggest( String type, String name) {
        this.type = type;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
